package com.fhi.questionnaire.vo;



public class DeptExcelBean {
	
	 
	private  String deptName;
	
	
	private int deptVoteCount; 
	
	private int deptPopCount;
	
	private String  deptPercent;

	public String getDeptName() {
		return deptName;
	}

	public void setDeptName(String deptName) {
		this.deptName = deptName;
	}

	public int getDeptVoteCount() {
		return deptVoteCount;
	}

	public void setDeptVoteCount(int deptVoteCount) {
		this.deptVoteCount = deptVoteCount;
	}

	public int getDeptPopCount() {
		return deptPopCount;
	}

	public void setDeptPopCount(int deptPopCount) {
		this.deptPopCount = deptPopCount;
	}

	public String getDeptPercent() {
		return deptPercent;
	}

	public void setDeptPercent(String deptPercent) {
		this.deptPercent = deptPercent;
	}
	
}
